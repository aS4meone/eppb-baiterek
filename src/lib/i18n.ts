export type Locale = "ru" | "kk";
export const LOCALE_COOKIE = "eppb_locale";

export async function getLocale(): Promise<Locale> {
  const v = (await cookies()).get(LOCALE_COOKIE)?.value;
  return v === "kk" ? "kk" : "ru";
}

